#include<stdio.h>
int main(){
	printf("Hello World");
	printf("\nHello World\n");// \n is for new or next line
	printf("\tHello World");// \t is for a tab
	printf("\n\\Hello World%%");// double \\ to print single \ same for %
	printf("\nHello World\a");// \a for a windows beep sound effect
	// // or /*hi*/or /*__*/ used for comment line
	// // for a single line comment a whole line
	// /* -> comment opent, */->comment close, /*__*/ is for multi level comment 
	return 0;
}